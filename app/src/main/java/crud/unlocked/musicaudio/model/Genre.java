public class Genre {
    private String genre;
    private String title;
    private String photoFirst;
    private String photoSecond;
    private String photoThird;

    public String getGenre() {
        return genre;
    }

    public void setGenre(String genre) {
        this.genre = genre;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getPhotoFirst() {
        return photoFirst;
    }

    public void setPhotoFirst(String photoFirst) {
        this.photoFirst = photoFirst;
    }

    public String getPhotoSecond() {
        return photoSecond;
    }

    public void setPhotoSecond(String photoSecond) {
        this.photoSecond = photoSecond;
    }

    public String getPhotoThird() {
        return photoThird;
    }

    public void setPhotoThird(String photoThird) {
        this.photoThird = photoThird;
    }
}
